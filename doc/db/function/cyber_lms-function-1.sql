				        , LESSON_SCORE_AVG + MIDDLE_TEST_SCORE_AVG + LAST_TEST_SCORE_AVG + TEST_SCORE_AVG + ASSIGNMENT_SCORE_AVG + FORUM_SCORE_AVG + QUIZ_SCORE_AVG + RESH_SCORE_AVG AS CAL_TOT_SCR
				     FROM (
				   	 	   SELECT  X.CRS_CRE_CD
							     , X.STD_NO
							     , ROUND((X.LESSON_SCORE/100) * X.LESSON_SCORE_RATIO, 2) AS LESSON_SCORE_AVG
							     , ROUND((X.MIDDLE_TEST_SCORE/100) * X.MIDDLE_TEST_SCORE_RATIO, 2) AS MIDDLE_TEST_SCORE_AVG
							     , ROUND((X.LAST_TEST_SCORE/100) * X.LAST_TEST_SCORE_RATIO, 2) AS LAST_TEST_SCORE_AVG
							     , ROUND((X.TEST_SCORE/100) * X.TEST_SCORE_RATIO, 2) AS TEST_SCORE_AVG
							     , ROUND((X.ASSIGNMENT_SCORE/100) * X.ASSIGNMENT_SCORE_RATIO, 2) AS ASSIGNMENT_SCORE_AVG
							     , ROUND((X.FORUM_SCORE/100) * X.FORUM_SCORE_RATIO, 2) AS FORUM_SCORE_AVG
							     , ROUND((X.QUIZ_SCORE/100) * X.QUIZ_SCORE_RATIO, 2) AS QUIZ_SCORE_AVG
							     , ROUND((X.RESH_SCORE/100) * X.RESH_SCORE_RATIO, 2) AS RESH_SCORE_AVG
							  FROM 
							  (
								SELECT A.STD_NO
								     , C.CRS_CRE_CD
								     , A.SCORE_STATUS
								     , A.SCORE_GRADE 
								     , ((SELECT DEPT_NM FROM TB_HOME_USER_INFO  WHERE USER_ID = (SELECT USER_ID FROM TB_LMS_STD WHERE STD_NO = A.STD_NO))) AS DEPT_NM
								     , (SELECT USER_ID FROM TB_LMS_STD WHERE STD_NO = A.STD_NO) AS USER_ID
								     , ((SELECT X.USER_NM FROM TB_HOME_USER_INFO X  WHERE X.USER_ID = (SELECT XX.USER_ID FROM TB_LMS_STD XX WHERE XX.STD_NO = A.STD_NO))) AS USER_NM
								     , A.TOT_SCORE 
								     , A.FINAL_SCORE 
								     , A.ADD_SCORE AS ETC_SCORE
								     , A.PROF_MEMO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '1', B.GET_SCORE, '-')) AS LESSON_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '1', C.SCORE_RATIO , '-')) AS LESSON_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '1', B.SCORE_ITEM_ID, '-')) AS LESSON_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '2', B.GET_SCORE, '-')) AS MIDDLE_TEST_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '2', C.SCORE_RATIO, '-')) AS MIDDLE_TEST_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '2', B.SCORE_ITEM_ID, '-')) AS MIDDLE_TEST_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '3', B.GET_SCORE, '-')) AS LAST_TEST_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '3', C.SCORE_RATIO, '-')) AS LAST_TEST_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '3', B.SCORE_ITEM_ID, '-')) AS LAST_TEST_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '4', B.GET_SCORE, '-')) AS TEST_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '4', C.SCORE_RATIO, '-')) AS TEST_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '4', B.SCORE_ITEM_ID, '-')) AS TEST_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '5', B.GET_SCORE, '-')) AS ASSIGNMENT_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '5', C.SCORE_RATIO, '-')) AS ASSIGNMENT_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '5', B.SCORE_ITEM_ID, '-')) AS ASSIGNMENT_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '6', B.GET_SCORE, '-')) AS FORUM_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '6', C.SCORE_RATIO, '-')) AS FORUM_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '6', B.SCORE_ITEM_ID, '-')) AS FORUM_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '7', B.GET_SCORE, '-')) AS QUIZ_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '7', C.SCORE_RATIO, '-')) AS QUIZ_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '7', B.SCORE_ITEM_ID, '-')) AS QUIZ_ITEM_ID
								     , MAX(IF(C.SCORE_ITEM_ORDER = '8', B.GET_SCORE, '-')) AS RESH_SCORE
								     , MAX(IF(C.SCORE_ITEM_ORDER = '8', C.SCORE_RATIO, '-')) AS RESH_SCORE_RATIO
								     , MAX(IF(C.SCORE_ITEM_ORDER = '8', B.SCORE_ITEM_ID, '-')) AS RESH_ITEM_ID
								  FROM TB_LMS_STD_SCORE A
								  JOIN TB_LMS_STD_SCORE_ITEM B ON (B.STD_NO = A.STD_NO)
								  JOIN TB_LMS_STD_SCORE_ITEM_CONF C ON (C.SCORE_ITEM_ID = B.SCORE_ITEM_ID)
								  JOIN TB_LMS_STD D ON (D.STD_NO = A.STD_NO)
								 GROUP BY A.STD_NO, C.CRS_CRE_CD
								) X
							WHERE 1=1
							  AND X.CRS_CRE_CD = IN_CRS_CRE_CD
						) XX
					) XXX
				) Y
		 		WHERE Y.MARK_RANK = V_MRKS_RANK
				ORDER BY Y.RNO DESC
			) YY;

        IF V_MRKS_RANK_TEMP IS NOT NULL THEN
            SET V_MRKS_RANK := V_MRKS_RANK_TEMP;
        END IF;
    END IF;

    RETURN IFNULL(V_MRKS_RANK,0); 
END

-- FN_SCOR_LATE_DMARK_SCR
CREATE FUNCTION FN_SCOR_LATE_DMARK_SCR (
		IN_CONT_CNT INT, 
		IN_ATTD_SCR FLOAT, 
		IN_LATE_CNT INT
	) RETURNS float(5,2)
	DETERMINISTIC
	COMMENT '성적 등급 비율 계산'
BEGIN
	DECLARE V_RESULT FLOAT(5,2) DEFAULT 0;
	DECLARE V_RATE   FLOAT(5,2) DEFAULT 0;

	IF IN_LATE_CNT <= 1 THEN
        SET V_RATE := 1; 
    ELSEIF IN_LATE_CNT >= 2 AND  IN_LATE_CNT <= 3 THEN
        SET V_RATE := 0.95;
    ELSEIF IN_LATE_CNT >= 4 AND  IN_LATE_CNT <= 5 THEN
        SET V_RATE := 0.90;    
    ELSEIF IN_LATE_CNT >= 6 AND  IN_LATE_CNT <= 7 THEN
        SET V_RATE := 0.80;
    ELSEIF IN_LATE_CNT >= 8 AND  IN_LATE_CNT <= 9 THEN
        SET V_RATE := 0.70;
    ELSEIF IN_LATE_CNT >= 10 AND  IN_LATE_CNT <= 11 THEN
        SET V_RATE := 0.60;
    ELSEIF IN_LATE_CNT >= 12 AND  IN_LATE_CNT <= 13 THEN
        SET V_RATE := 0.50;
    END IF;

    SET V_RESULT := IN_ATTD_SCR * V_RATE;

    RETURN V_RESULT;
END

-- FN_PASSWORD
